// Chapter One: Writing My First Program in Java
// -------------------------------------------------
// In this program, we will learn the basic structure
// of a Java program and how to print text on the screen.
// The program will display "Hello World" as output.

public class Ch1_HelloWorld {  // Every Java program must have a class. Here, the class name is "HelloWorld".

    // The main() method is the entry point of every Java program.
    // When we run the program, execution starts from here.
    public static void main(String[] args) {

        // System.out.println() is used to print text to the console.
        // "System" is a built-in class, "out" is the output stream,
        // and "println()" is a method that prints text followed by a new line.

        System.out.println("Hello World");

    }
}
